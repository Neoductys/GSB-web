	<h2>Le Laboratoire</h2>
	
<p>Le laboratoire <b>Galaxy Swiss Bourdin</b> est né suite à la fusion entre <b>Galaxy</b> (laboratoire américain spécialisé dans les maladies virales comme le SIDA et les hépatites) 
et le conglomérat <b>Swiss Bourdin</b> formé par l'union de trois petits laboratoires.
En 2009, les deux laboratoires s’unissent pour devenir leader de l'industrie pharmaceutique, 
l'entité Galaxy Swiss Bourdin Europe est établie à Paris et le siège de la multinationale est à Philadelphie en Pennsylvanie.
</p>
<p>
Le laboratoire Galaxy Swiss Bourdin compte en France <b>540 visiteurs médicaux</b> dont 60 dans les départements et territoires d'outre-mer .
</p>

	<h2>Le Visiteur</h2>
<p>Ils effectuent des visites auprès des médecins, pharmaciens, infirmières et autre .
Ces visites ont pour objectif de tenir les professionnels de la santé au courant sur les différents produits de l'entreprise.
</p>
<p>Les visiteurs n'ont pas pour mission de vendre des produits mais ont un impact important sur la prescription des médicaments du laboratoire.
</p>
	<p>Une organisation précise a été adoptée pour gérer les visites sur les six secteurs 
	(Paris-Centre, Nord, Sud, Est, Ouest, DTOM Caraïbes-Amériques, DTOM Asie-Afrique), 
	cette organisation est établie selon un système hiérarchique par région et à un niveau supérieur.
</p>
<center><img src="images/org.jpg"/></center>
</br>
<p>
Les frais engendrés par les déplacements des visiteurs sont pris en charge par la comptabilité.</p>
<p>Galaxy garde son principe d’engagements des frais qui est celui de la carte bancaire au nom de l’entreprise. Chez Swiss Bourdin une gestion des frais permet de limiter les justificatifs.</p></br>
<p>Les visiteurs rapporteront directement sur le terrain les informations qui concernent </p>
<ul>
